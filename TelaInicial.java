    private void btnEXLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEXLActionPerformed
        // TODO add your handling code here:
        if(cmb.getSelectedIndex() == AUTOR){
            TelaExlAutor telaExlAutor = new TelaExlAutor();
            telaExlAutor.setVisible(true);
        }
        
        if(cmb.getSelectedIndex() == LEITOR){
            TelaExlLeitor telaExlLeitor = new TelaExlLeitor();
            telaExlLeitor.setVisible(true);
        }
            
        
        if(cmb.getSelectedIndex() == LIVRO){
            TelaExlLivro telaExlLivro = new TelaExlLivro();
            telaExlLivro.setVisible(true);
        }
          
        if(cmb.getSelectedIndex() == AUTORDOLIVRO){
            TelaExcluiAutorDoLivro telaExcluiAutorDoLivro = new TelaExcluiAutorDoLivro();
            telaExcluiAutorDoLivro.setVisible(true);
        }
        

    }//GEN-LAST:event_btnEXLActionPerformed

